    public Listado() {
        initComponents();
       
        
        modelo= new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido Paterno");
        modelo.addColumn("Apellido Materno");
        modelo.addColumn("Estatus");
        modelo.addColumn("Observaciones");
       
        
        this.jTable1.setModel(modelo);
        this.jTable1.setRowHeight(70);
        
        MongoConnection mongoObject = new MongoConnection();
        mongoObject.crearConexion();

        List<String> documents = mongoObject.getRows();

        for(String document : documents){
            BasicBSONObject documentObject = ( BasicBSONObject ) JSON.parse( document );
            modelo.addRow(new Object[]{documentObject.get("Nombre"), documentObject.get("Apellido Paterno"), documentObject.get("Apellido Materno"),documentObject.get("Estatus"),documentObject.get("Observaciones")});
        }
        
        
    }

  
